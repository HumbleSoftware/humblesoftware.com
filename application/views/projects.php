<div class="project-summary">
	
    <span class="icon">
        <a href="<?php echo base_url(); ?>transcriber/index"><img alt="Project: HumbleTranscriber, HTML5 Transcription Tool" src="<?php static_image(); ?>products/transcriber-icon.jpg" /></a>
    </span>
    
	<p>
    <a href="<?php echo base_url(); ?>transcriber/index">HumbleTranscriber</a> 
	is an HTML5 video transcription application designed with ease of use in mind.  
	It allows the user to do line-by-line transcription of audio or video, edit
	existing transcriptions, and create translations.  Developers can easily
	extend HumbleTranscriber to create a new application as well as integrate
	the tool with existing applications.
	</p>
	
	<p>
	Written entirely in JavaScript, the tool provides several players including 
	JW Player, YouTube, and HTML5.  It also exposes an AJAX based API for 
	integration with a server.  Visit the <a href="<?php echo base_url(); ?>transcriber/demo">demo</a>
	or check out LINK HERE to see a full transcription application in action.
	</p>
</div>

